public class Contribution
{
    public static void main(String[] args)
    {
        double monthlyPay, contribution;
        
        monthlyPay = 6000;
        
        contribution = .05;
        System.out.println("5 percent is $" + monthlyPay * contribution + " per month.");
        
        contribution = .08;
        System.out.println("8 percent is $" + monthlyPay * contribution + " per month.");
        
        contribution = .10;
        System.out.println("10 percent is $" + monthlyPay * contribution + " per month.");
        
    }
}
        